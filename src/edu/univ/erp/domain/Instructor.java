package edu.univ.erp.domain;

public class Instructor{
    private String user_ID;
    private String program;
    private int year;
    private String email_id;

    public String getUser_ID(){
        return user_ID;
    }

    public String getProgram(){
        return program;
    }

    public int getYear(){
        return year;
    }

    public String getEmailId(){
        return email_id;
    }
}